                            <h1 class="mb-3 text-secondary text-uppercase font-weight-bold">Asean members back peaceful solution to China-Taiwan row</h1>
                            <p> Phnom Penh—Member states of the Association of Southeast Asian Nations (Asean) still support the “One China Policy” and believe that China and Taiwan should work to peacefully resolve their dispute. </p>

                            <p>  President Marcos said this was the consensus among nations belonging to the regional bloc during discussions at the recently-concluded 40th and 41st Asean Summits here. </p>
                              
                            <p>  “Especially problems on Taiwan Strait, most of us are for the One China Policy. So we’re saying, we are still following the One China Policy. But we just want peace,” Mr. Marcos told reporters in an interview. </p>
                              
                            <p>  He went on: “We follow the One China Policy, it’s an internal matter. We believe that Taiwan is part of China, but you must resolve those issues peacefully. That is what the Asean is asking. That there will be no trouble.” </p>
                              
                            <p>  The One China Policy is the diplomatic acknowledgment of the position held by the People’s Republic of China that there is only a single sovereign state under the name China. China is not part of the Asean but is one of its key dialogue partners. </p>
                              
                            <p>  Mr. Marcos was asked about developments in the South China Sea maritime dispute during the Asean Summits here. </p>
                              
                            <p>  It may be recalled that Mr. Marcos raised the territorial conflict twice during the regional meet—when he pushed for the completion of the Code of Conduct on the South China Sea between the Asean and China, and when he urged nations to work together to ensure that the territory remains a “sea of peace, security, stability and prosperity.” </p>
                              
                            <p>  Addressing state leaders at the 17th East Asia Summit on Sunday, he said the South China Sea would be a “nexus of vibrant economic engagements and interactions, not an epicenter of armed conflict or geopolitical maneuverings” as long as international law and the United Nations Convention on the Law of the Sea (Unclos) is upheld. </p>
                              
                            <p>  The President told reporters here that he was not the only state leader to bring up the maritime dispute involving China and a handful of nations. </p>
                              
                            <p>  “On the South China Sea, everybody, including the Chinese premier, says we follow the Unclos and international law,” Mr. Marcos said. “That was the essential message to all involved, including the Americans, on the West Philippine Sea, the South China Sea,” he added. </p>
                              
                            <p>  Asked if he was optimistic that there would be significant progress on the completion of the Code of Conduct after the Asean Summit, the President remarked that “nothing new actually happened.” </p>
                              
                            <p>  “We all just restated over and over again, that a Code of Conduct is needed. Because to be very clear about what it is that we are agreeing upon, we need to have a Code of Conduct,” he said. </p>
                              
                            <p>  “There’s been some progress in the past year, but we really need to have the actual Code of Conduct, already finalized and already in place as soon as possible,” Mr. Marcos added. </p>     
